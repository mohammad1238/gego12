#String boolean-returned-values methods 
str1='hello 65'

print(str1.isdigit()) #all of str1’s characters are digits
print(str1.isalpha()) #all of str1’s characters are letters of the alphabet
print(str1.isalnum()) #all of str1’s characters are letters of the alphabet or digits
print(str1.islower()) #str1 has at least 1 alphabetic character and all of its alphabetic characters arelowercase
print(str1.isupper()) #str1 has at least 1 alphabetic character and all of its alphabetc characters areuppercase
print('   '.isspace()) #str1 contains only whitespace characters
